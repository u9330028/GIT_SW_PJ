BOOL KS_code_to_cho_jung_jong(char *str, char *cho, char *jung, char *jong)
{
	//KS 완성형 코드를 초성,중성,종성으로 나누다.
	//한글이 아니라면 모두 0을 반환한다.
	//주의사항 : 초성과 중성만 있는 경우 종성에는 공백문자가 2개 온다.
	//예) 코 => ㅋ + ㅗ + '__'
	//예) ㅠ => '__' + ㅠ + '__'

	INT16 combi;
	char buf[3];

	if((*str&0x8000)==0)
	{
		//한글이 아니다
		*((INT16*)cho)=KS_NULL;
		*((INT16*)jung)=KS_NULL;
		*((INT16*)jong)=KS_NULL;
		return FALSE;
	}
	//완성형을 조합형으로 변환한다.
	KS_to_COMBI(buf, str);
	
	//초성  0111 1100 0000 0000
	cho[1]=buf[0] & 0xfc;		//1xxx xx00 0100 0001
	cho[0]=0x41;
	
	//중성  0000 0011 1110 0000
	jung[1]=(buf[0] & 0x03) | 0x84;		//1000 01xx xxx0 0001
	jung[0]=(buf[1] & 0xe0) | 0x01;
	
	//종성  0000 0000 0001 1111
	jong[1]=(char)0x84;						//1000 0100 010x xxxx
	jong[0]=(buf[1] & 0x1f) | 0x40;
	
	//조합형 한글을 완성형으로 바꾼다.
	
	combi=HCODE_JASO_cmb2ks(*(INT16*)cho);
	cho[1]=combi&0xff;
	cho[0]=combi >> 8;

	combi=HCODE_JASO_cmb2ks(*(INT16*)jung);
	jung[1]=combi&0xff;
	jung[0]=combi >> 8;

	combi=HCODE_JASO_cmb2ks(*(INT16*)jong);
	jong[1]=combi&0xff;
	jong[0]=combi >> 8;

	cho[2]='\0';
	jung[2]='\0';
	jong[2]='\0';
	return TRUE;
}

int Han_GetChar(char *hchar, char *s)
{
	//문자열 s로부터 (한글을 인식하는) 1문자를 hchar에 복사한다.
	//복사된 문자열의 길이를 반환한다.
	//예) *s="한글";  => hchar="한"  return=2;
	//예) *s="A한글";  => hchar="A"  return=1;
	

	//한글이면 2바이트 복사, 반환
	if(((*s)&0x80)!=0)
	{
		//한글이다
		*hchar=*s;
		hchar++;

		s++;
		*hchar=*s;

		hchar++;
		*hchar='\0';
		return 2;
	}
	else
	{
		//영문
		*hchar=*s;
		hchar++;

		*hchar='\0';
		return 1;
	}
}
