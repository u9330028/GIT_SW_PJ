/*------------------------------------------------------------
	완성형 중성+중성 결합한다.
-------------------------------------------------------------*/
UINT16 HCODE_JASO_mix_vowel(UINT16 vowel1, UINT16 vowel2)
{
	switch( vowel1 )
	{
		case 0x8441:		return vowel2;
		case 0xa4bf:
			if(vowel2 == 0xa4d3)  return 0xa4c0; // ㅏ + ㅣ=ㅐ
			break;
		case 0xa4c1:
			if(vowel2 == 0xa4d3)  return 0xa4c2; // ㅑ + ㅣ=ㅒ
			break;
		case 0xa4c3 :
			if(vowel2 == 0xa4d3)  return 0xa4c0; // ㅓ + ㅣ=ㅔ
			break;
		case 0xa4c5 :
			if(vowel2 == 0xa4d3)  return 0xa4c6; // ㅕ + ㅣ=ㅖ
			break;
		case 0xa4c7:
			switch( vowel2 )
			{
				case 0xa4d3:      return 0xa4ca; // ㅗ + ㅣ=ㅚ
				case 0xa4bf:      return 0xa4c8; // ㅗ + ㅏ=ㅘ
				case 0xa4c0:      return 0xa4c9; // ㅗ + ㅐ=ㅙ
			}
			break;
		case 0xa4cc:
			switch( vowel2 )
			{
				case 0xa4d3:      return 0xa4cf; // ㅜ + ㅣ=ㅟ
				case 0xa4c3:      return 0xa4cd; // ㅜ + ㅓ=ㅝ
				case 0xa4c4:      return 0xa4ce; // ㅜ + ㅔ=ㅞ
			}
			break;
		case 0xa4d1:
			if(vowel2 == 0xa4d3)  return 0xa4d2; // ㅡ + ㅣ=ㅢ
	}
	
	return 0;
}
/*-------------------------------------------------------------
	완성형 종성+종성 결합한다.
-------------------------------------------------------------*/
UINT16 HCODE_JASO_mix_consonant(UINT16 cons1, UINT16 cons2)
{
	switch( cons1 )
	{
		case 0x8441:		return cons2;
		case 0xa4a1:
			if(cons2 == 0xa4b5)   return 0xa4a3; // ㄱ + ㅅ=ㄳ
			break;
		case 0xa4a4:
			if (cons2 == 0xa4b8)  return 0xa4a5; // ㄴ + ㅈ=ㄵ
			if (cons2 == 0xa4be)  return 0xa4a6; // ㄴ + ㅎ=ㄶ
			break;
		case 0xa4a9:
			switch( cons2 )
			{
				case 0xa4a1:      return 0xa4aa; // ㄹ + ㄱ=ㄺ
				case 0xa4b1:      return 0xa4ab; // ㄹ + ㅁ=ㄻ
				case 0xa4b2:      return 0xa4ac; // ㄹ + ㅂ=ㄼ
				case 0xa4b5:      return 0xa4ad; // ㄹ + ㅅ=ㄽ
				case 0xa4bc:      return 0xa4ae; // ㄹ + ㅌ=ㄾ
				case 0xa4bd:      return 0xa4af; // ㄹ + ㅍ=ㄿ
				case 0xa4be:      return 0xa4b0; // ㄹ + ㅎ=ㅀ
			}
			break;
		case 0xa4b2: 
			if(cons2 == 0xa4b5)   return 0xa4b4; // ㅂ + ㅅ=ㅄ
	}
	return 0;
}
